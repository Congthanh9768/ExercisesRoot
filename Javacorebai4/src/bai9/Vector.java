package bai9;

public class Vector {
    private int X;
    private int Y;

    public Vector() {
    }
    
    public Vector(int X, int Y) {
        this.X = X;
        this.Y = Y;
    }
    

    public int getX() {
        return X;
    }

    public void setX(int X) {
        this.X = X;
    }

    public int getY() {
        return Y;
    }

    public void setY(int Y) {
        this.Y = Y;
    }

    
    
    
}
